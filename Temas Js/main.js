




/* Metodo charAt */

// devuelve la posición del carácter que le pasemos como parámetro en el interior de la cadena.

// Si la cadena contiene varias ocurrencias del carácter que le pasemos como parámetro, 
// el método charAt devolverá solamente la posición de la primera de ellas. 
// Si el carácter no está presente en la cadena, el método charAt devolverá una cadena vacía.

let cadena = 'JavaScript';
let caracter = cadena.charAt(5); // 'J'

console.log(caracter)