/* Metodo charCodeAt */

// devuelve el codigo Unicode de los caracteres que esten en la posición que le pasemos.
// El método CharCodeAt es aceptado por las cadenas en JavaScript, 
// devolviendo el código Unicode de 16 bits del carácter que esté en la posición que le pasemos como parámetro al método.

let codigo1 = 'JavaScript'.charCodeAt(0) // 74
let codigo2 = 'JavaScript'.charCodeAt(1) // 97
let codigo3 = 'JavaScript'.charCodeAt(2) // 118
let codigo4 = 'JavaScript'.charCodeAt(3) // 97

console.log(codigo1)
console.log(codigo2)
console.log(codigo3)
console.log(codigo4)